
public class LoopConcepts {

	public static void main(String[] args) {
		//1.while lpop
		//print 1-10
		int i=1;//initialization
		while(i<=10) { //conditional
			System.out.println(i);
			i=i+1; //incremental/decremental

			
		}

	


//2.For loop
		//print 1-10
for( i=1;i<=10;i++) {
	System.out.println(i);

}



//print 10-1
for(i=10;i>=1;i--) {
	System.out.println(i);
}
	}
}


